-- A GPU-accelerated cross-platform terminal emulator
-- https://wezfurlong.org/wezterm/
-- Pull in the wezterm API
local wezterm = require("wezterm")

-- This will hold the configuration.
local config = wezterm.config_builder()

-- For example, changing the color scheme:
-- config.color_scheme = "Konsolas"

config.colors = {
	foreground = "#dcd7ba",
	background = "#1f1f28",

	cursor_bg = "#c8c093",
	cursor_fg = "#c8c093",
	cursor_border = "#c8c093",

	selection_fg = "#c8c093",
	selection_bg = "#2d4f67",

	scrollbar_thumb = "#16161d",
	split = "#16161d",

	ansi = { "#090618", "#c34043", "#76946a", "#c0a36e", "#7e9cd8", "#957fb8", "#6a9589", "#c8c093" },
	brights = { "#727169", "#e82424", "#98bb6c", "#e6c384", "#7fb4ca", "#938aa9", "#7aa89f", "#dcd7ba" },
	indexed = { [16] = "#ffa066", [17] = "#ff5d62" },
}

-- Change the font to Jetbrain Mono
config.font = wezterm.font("JetBrainsMono Nerd Font")

-- Change padding of the window
config.window_padding = {
	left = 2,
	right = 2,
	top = 0,
	bottom = 0,
}

-- Remove title bar
config.window_decorations = "NONE"

-- Background image
config.window_background_image = "/home/christiaan/wallpapers/trumpet-blurred-terminal-wallpapers.jpg"

-- Background settings
config.window_background_image_hsb = {
	-- Darken the background image by reducing it to 1/3rd
	brightness = 0.2,

	-- You can adjust the hue by scaling its value.
	-- a multiplier of 1.0 leaves the value unchanged.
	hue = 1.0,

	-- You can adjust the saturation also.
	saturation = 1.0,
}

-- Disable tab bar
config.enable_tab_bar = false

-- Size of opening window
config.initial_cols = 800
config.initial_rows = 240

-- Size of font
config.font_size = 10

-- Run Zellij upon startup of Wezterm with the layout in the layout folder of zellij
config.default_prog = { "zellij" }

-- and finally, return the configuration to wezterm
return config
